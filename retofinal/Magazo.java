
package retofinal;


public class Magazo {
    
    public int mpoder;
    public int mnumhadas;
    public int contadordebonks; 
    
    Magazo(int mpoder, int mnumhadas, int contadordebonks){
     
    this.mpoder = mpoder;
    this.mnumhadas = mnumhadas;
    this.contadordebonks = contadordebonks;
        
    }
    
    public int getmpoder(){
        return mpoder;
    }
    
    public void setmpoder(int mpoder){
    this.mpoder = mpoder;
    }

    public int getmnumhadas(){
        return mnumhadas;
    }
    
    public void setmnumhadas(int mnumhadas){
    this.mnumhadas = mnumhadas;
    }    

    public int getcontadordebonks(){
        return contadordebonks;
    }
    
    public void setcontadordebonks(int contadordebonks){
    this.contadordebonks = contadordebonks;
    }    
    
  
    }
    

